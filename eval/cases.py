"""Labeled evaluation cases.

Each case is an image plus the claimed application data and the TRUE expected
per-field verdict. "Clean" cases are the bundled synthetic samples; "degraded"
cases are programmatically distorted copies of a compliant label that simulate
real-world phone photos (rotation, blur, compression, low contrast). The eval
measures whether the system reaches the TRUE verdict on each.

Drop real label photos into eval/images/real/ and add cases here to extend the
set; the runner will pick them up.
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class EvalCase:
    name: str
    image: str            # path relative to repo root
    brand: str
    alcohol_content: str
    kind: str             # "clean" | "degraded" | "real"
    # TRUE expected verdicts (what a perfect reader should conclude):
    exp_brand: bool
    exp_alcohol: bool
    exp_warning: bool


# The three bundled synthetic samples (ground truth from how they were generated).
CLEAN_CASES = [
    EvalCase("clean_pass", "app/static/samples/clean_pass.png",
             "Stone's Throw", "5.0", "clean", True, True, True),
    EvalCase("abv_mismatch", "app/static/samples/abv_mismatch.png",
             "Stone's Throw", "5.0", "clean", True, False, True),
    EvalCase("bad_warning", "app/static/samples/bad_warning.png",
             "Stone's Throw", "5.0", "clean", True, True, False),
]

# Degraded variants are generated from clean_pass (a fully-compliant label),
# so the TRUE verdict for every field is PASS. Generated by run_eval.py.
DEGRADED_SPECS = [
    ("degraded_rotate", "rotate"),
    ("degraded_blur", "blur"),
    ("degraded_jpeg", "jpeg"),
    ("degraded_lowcontrast", "lowcontrast"),
]
