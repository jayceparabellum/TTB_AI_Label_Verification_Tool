"""Labeled evaluation cases.

Each case is an image plus the claimed application data and the TRUE expected
per-field verdict. "Clean" cases are the bundled synthetic samples; "degraded"
cases are programmatically distorted copies of a compliant label that simulate
real-world phone photos (rotation, blur, compression, low contrast). The eval
measures whether the system reaches the TRUE verdict on each.

Drop real label photos into eval/images/real/ and add cases here to extend the
set; the runner will pick them up.
"""

from __future__ import annotations

from dataclasses import dataclass

from app.samples import SAMPLES


@dataclass(frozen=True)
class EvalCase:
    name: str
    image: str            # path relative to repo root
    brand: str
    alcohol_content: str
    kind: str             # "clean" | "degraded" | "real"
    # TRUE expected verdicts (what a perfect reader should conclude):
    exp_brand: bool
    exp_alcohol: bool
    exp_warning: bool


# Per-sample expected verdicts (brand, alcohol, warning).
_EXPECTED_VERDICTS: dict[str, tuple[bool, bool, bool]] = {
    "clean_pass": (True, True, True),
    "abv_mismatch": (True, False, True),
    "bad_warning": (True, True, False),
}

# Generate clean cases from the canonical Sample definitions in app/samples.
CLEAN_CASES = [
    EvalCase(
        name=sample.key,
        image=str(sample.path),
        brand=sample.brand,
        alcohol_content=sample.alcohol_content,
        kind="clean",
        exp_brand=verdicts[0],
        exp_alcohol=verdicts[1],
        exp_warning=verdicts[2],
    )
    for sample in SAMPLES.values()
    if (verdicts := _EXPECTED_VERDICTS.get(sample.key)) is not None
]

# Degraded variants are generated from clean_pass (a fully-compliant label),
# so the TRUE verdict for every field is PASS. Generated by run_eval.py.
# Each simulates a way a real phone photo of a compliant label goes wrong:
# rotation, blur, JPEG compression, low/uneven lighting, perspective, sensor
# noise, and compound distortions.
DEGRADED_SPECS = [
    ("degraded_rotate", "rotate"),
    ("degraded_rotate_heavy", "rotate_heavy"),
    ("degraded_blur", "blur"),
    ("degraded_jpeg", "jpeg"),
    ("degraded_lowcontrast", "lowcontrast"),
    ("degraded_perspective", "perspective"),   # photographed at an angle (keystone)
    ("degraded_glare", "glare"),               # bright wash / overexposure
    ("degraded_shadow", "shadow"),             # uneven lighting, one side dark
    ("degraded_noise", "noise"),               # low-light sensor grain
    ("degraded_blur_rotate", "blur_rotate"),   # compound: mild blur + skew
]
